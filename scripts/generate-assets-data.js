import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const publicDir = path.join(__dirname, '../public');
const outputDir = path.join(__dirname, '../src/data');
const outputFile = path.join(outputDir, 'assetsData.js');

// Helper to recursively get files
function getFilesRecursive(dir, baseDir = dir) {
  let results = [];
  if (!fs.existsSync(dir)) return results;
  const list = fs.readdirSync(dir);
  for (const file of list) {
    const filePath = path.join(dir, file);
    const stat = fs.statSync(filePath);
    if (stat && stat.isDirectory()) {
      results = results.concat(getFilesRecursive(filePath, baseDir));
    } else {
      // Get relative path with forward slashes
      const relativePath = path.relative(baseDir, filePath).replace(/\\/g, '/');
      results.push(relativePath);
    }
  }
  return results;
}

function generate() {
  console.log('Generating assets metadata...');

  // Clients
  const clientsDir = path.join(publicDir, 'images/clients');
  const clientFiles = fs.existsSync(clientsDir) 
    ? fs.readdirSync(clientsDir).filter(file => {
        const stat = fs.statSync(path.join(clientsDir, file));
        return !stat.isDirectory() && /\.(png|jpg|jpeg|webp|avif)$/i.test(file);
      })
    : [];

  // Gallery
  const galleryDir = path.join(publicDir, 'gallery');
  const galleryFiles = getFilesRecursive(galleryDir).filter(file => {
    return /\.(png|jpg|jpeg|webp|avif)$/i.test(file);
  });

  // Ensure output directory exists
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  const content = `// This file is auto-generated by scripts/generate-assets-data.js during build/dev
// Do not edit this file manually.

export const clientFiles = ${JSON.stringify(clientFiles, null, 2)};

export const galleryFiles = ${JSON.stringify(galleryFiles, null, 2)};
`;

  fs.writeFileSync(outputFile, content, 'utf-8');
  console.log(`Successfully generated ${outputFile}`);
}

generate();
